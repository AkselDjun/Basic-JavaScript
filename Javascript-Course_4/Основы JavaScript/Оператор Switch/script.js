var carColor = "black";

switch (carColor) {
    case "green":
        console.log("Цвет машины зеленый");
        break;
    case "black":
        console.log("Цвет машины черный");
        break;
    case "red":
        console.log("Цвет машины красный");
        break;
    default:
        console.log("Цвет машины не определён");
};